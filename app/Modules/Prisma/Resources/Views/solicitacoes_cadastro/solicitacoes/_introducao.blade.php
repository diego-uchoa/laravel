	         			a partir da informação dos respectivos dados cadastrais e da atribuição de login e senha.
	         		</p>
	         		<p>Em princípio, quaisquer bancos, instituições financeiras, empresas não financeiras, entidades de pesquisa, consultorias, associações de classe, universidades, etc. podem 
