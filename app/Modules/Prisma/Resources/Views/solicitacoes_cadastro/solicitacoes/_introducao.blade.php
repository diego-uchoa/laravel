			          	à evolução das principais variáveis fiscais brasileiras. Ele oferece uma oportunidade para o aprimoramento dos estudos fiscais no país, além de facilitar o controle 
			          	social a partir da divulgação das expectativas de renomadas instituições sobre o desempenho da política fiscal nacional.
		          	</p>
		          	<p>Mais informações no <a href="http://www.spe.fazenda.gov.br/prisma-fiscal" target="_blank">site do Prisma Fiscal</a>.</b><p>
